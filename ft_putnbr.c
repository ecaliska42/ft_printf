int	ft_putnbr(int nb)
{
	int	i;

	i = 0;
	if (nb == -2147483648)
		return (write(1, "-2147483648", 11));
	if (nb < 0 && nb > -2147483648)
	{
		i += print('-');
		nb *= -1;
	}
	if (nb > 9)
	{
		i += ft_putnbr(nb / 10);
		i += ft_putnbr(nb % 10);
	}
	else if (nb < 10 && nb >= 0)
		i += print(nb + '0');
	return (i);
}
